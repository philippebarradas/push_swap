int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	e;

	e = 0;
	i = 0;
	if (s1[0] == '+')
		i++;
	if (s2[0] == '+')
		e++;
	while (s1[i] == s2[e] && s1[i] != '\0' && s2[e] != '\0')
	{
		i++;
		e++;
	}
	return (s1[i] - s2[e]);
}
